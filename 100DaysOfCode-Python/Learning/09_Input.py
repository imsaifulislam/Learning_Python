print("the value of subtraction",           x, "-", y, "=", int(x)-int(y))
print("the value of Multiplication",        x, "*", y, "=", int(x)*int(y))
print("the value of division",              x, "/", y, "=", int(x)/int(y))
print("the value of Floor Division",        x, "//", y, "=", int(x)//int(y))
